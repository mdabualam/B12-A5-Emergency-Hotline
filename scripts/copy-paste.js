// copy-1
document.getElementById('copy-1').addEventListener('click', async () => {
    const copiedContent = document.getElementById('number-1').innerText;
    try {
        await navigator.clipboard.writeText(copiedContent);
    }
    catch (err){
        console.error("Faild to copy", err);
    }
});

